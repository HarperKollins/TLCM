def generate_locomo_dataset():
    """Generate and save the 1k-scale LoCoMo dataset to JSON."""
    total_mems = sum(
        len(mems)
        for ws_epochs in LOCOMO_MEMORIES.values()
        for mems in ws_epochs.values()
    )
    dataset = {
        "metadata": {
            "version": "2.0",
            "benchmark": "TLCM-LoCoMo-Style",
            "scale": "1k",
            "total_memories": total_mems,
            "total_updates": len(LOCOMO_UPDATES),
            "total_questions": len(LOCOMO_QUESTIONS),
            "workspaces": list(LOCOMO_WORKSPACES.keys()),
            "epochs_per_workspace": {ws: [e["name"] for e in eps] for ws, eps in LOCOMO_EPOCHS.items()},
            "question_categories": {
                "point_in_time": len([q for q in LOCOMO_QUESTIONS if q["category"] == "point_in_time"]),
                "evolution": len([q for q in LOCOMO_QUESTIONS if q["category"] == "evolution"]),
                "isolation": len([q for q in LOCOMO_QUESTIONS if q["category"] == "isolation"]),
                "contradiction": len([q for q in LOCOMO_QUESTIONS if q["category"] == "contradiction"]),
                "decay": len([q for q in LOCOMO_QUESTIONS if q["category"] == "decay"]),
            },
        },
        "workspaces": LOCOMO_WORKSPACES,
        "epochs": LOCOMO_EPOCHS,
        "memories": LOCOMO_MEMORIES,
        "updates": [
            {"workspace": u[0], "epoch": u[1], "original_fragment": u[2],
             "new_content": u[3], "reason": u[4]}
            for u in LOCOMO_UPDATES
        ],
        "questions": LOCOMO_QUESTIONS,
    }

    RESULTS_DIR.mkdir(parents=True, exist_ok=True)
    out = RESULTS_DIR / "locomo_dataset.json"
    with open(out, "w", encoding="utf-8") as f:
        json.dump(dataset, f, indent=2, ensure_ascii=False)

    print(f"[LoCoMo Dataset] Generated: {out}")
    print(f"  Total memories : {total_mems}")
    print(f"  Total updates  : {len(LOCOMO_UPDATES)}")
    print(f"  Total questions: {len(LOCOMO_QUESTIONS)}")
    return dataset


if __name__ == "__main__":
    generate_locomo_dataset()
